export function getConcept(id: string): Concept | null {
  return ESTATE_CONCEPTS.find((c) => c.id === id) ?? null;
}

/**
 * Return true if all prerequisites of `conceptId` appear in `mastered`.
 * Mastery is determined by the caller — this only checks edge coverage.
 */
export function prerequisitesMet(
  conceptId: string,
  mastered: readonly string[]
): boolean {
  const c = getConcept(conceptId);
  if (!c) return false;
  const set = new Set(mastered);
  return c.prerequisites.every((p) => set.has(p));
}

/**
 * Topologically sort a list of concept IDs so prerequisites always come
 * first. Pure — returns a new array.
 */
export function topoSortConcepts(ids: readonly string[]): readonly string[] {
  const wanted = new Set(ids);
  const visited = new Set<string>();
  const out: string[] = [];
  function visit(id: string): void {
    if (visited.has(id) || !wanted.has(id)) return;
    visited.add(id);
    const c = getConcept(id);
    if (!c) return;
    for (const p of c.prerequisites) visit(p);
    out.push(id);
  }
  for (const id of ids) visit(id);
  return out;
}

/** All concepts in a given category. Pure filter. */
export function conceptsByCategory(category: Concept['category']): readonly Concept[] {
  return ESTATE_CONCEPTS.filter((c) => c.category === category);
}

/** Concepts at or below a given numeric difficulty rank. */
export function conceptsAtOrBelowDifficulty(rank: 1 | 2 | 3 | 4 | 5): readonly Concept[] {
  return ESTATE_CONCEPTS.filter((c) => c.difficultyRank <= rank);
}

/** Look up a worked example by id across the whole catalog. */
export function findWorkedExample(exampleId: string): WorkedExample | null {
  for (const c of ESTATE_CONCEPTS) {
    const m = c.workedExamples.find((w) => w.id === exampleId);
    if (m) return m;
  }
  return null;
}
